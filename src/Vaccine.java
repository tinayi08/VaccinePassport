public class Vaccine {

    String brand;
    int requiredShots;
    String oneShotDate;
    String twoShotDate;

    public Vaccine(String brand, int requiredShots, String oneShotDate, String twoShotDate) {
        this.brand = brand;
        this.requiredShots = requiredShots;
        this.oneShotDate = oneShotDate;
        this.twoShotDate = twoShotDate;
    }
}
